<p class="his">ABOUT  NCC</p>
<p class="para"> The National Cadet Corps is the Indian military cadet corps with its Headquarters at New Delhi. It is open to school and college students on voluntary basis. National Cadet Corps is a Tri-Services Organization, comprising the Army, Navy and Air Force, engaged in grooming the youth of the country into disciplined and patriotic citizens. The National Cadet Corps in India is a voluntary organization which recruits cadets from high schools, colleges and Universities all over India. The Cadets are given basic military training in small arms and parades. The officers and cadets have no liability for active military service once they complete their course but are given preference over normal candidates during selections based on the achievements in the corps.</p>
<p class="his">NCC HISTORY</p>
<p class="para">NCC was firstly started in 1666 in Germany. The NCC in India was formed with the National Cadet Corps Act of 1948. It was raised on 15 July 1948. The origin of NCC can be traced back to the ‘University Corps’, which was created under the Indian Defence Act 1917, with the objective to make up the shortage of the Army. In 1920, when the Indian Territorial Act was passed, the ‘University Corps’ was replaced by the University Training Corps (UTC). The aim was to raise the status of the UTC and make it more attractive to the youth. The UTC Officers and cadets dressed like the army. It was a significant step towards the Indianisation of armed forces. It was rechristened in the form of UOTC so the National Cadet Corps can be considered as a successor of the University Officers Training Corps (UOTC) which was established by the British Government in 1942. During World War II, the UOTC never came up to the expectations set by the British. This led to the idea that some better schemes should be formed, which could train more young men in a better way, even during peace. A committee headed by Pandit H.N. Kunzru recommended a cadet organization to be established in schools and colleges at a national level. The National Cadet Corps Act was accepted by the Governor General and on 15 July 1948 the National Cadet Corps came into existence.
</p>
<p class="para"> In 1949, the Girls Division was raised in order to give equal opportunities to school and college going girls. The NCC was given an inter-service image in 1950 when the Air Wing was added, followed by the Naval Wing in 1952. Same year, the NCC curriculum was extended to include community development/social service activities as a part of the NCC syllabus at the behest of Late Pandit Jawaharlal Nehru who took keen interest in the growth of the NCC. Following the 1962 Sino-Indian War, to meet the requirement of the Nation, the NCC training was made compulsory in 1963. In 1968, the Corps was again made voluntary.[3]</p>

